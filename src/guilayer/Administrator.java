    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String solapin;
        String cid;
        String nombre;
        String correo;
        if (txtSolapin.getText().trim().matches("^[A-Z]?\\d{6}$")) {
            solapin = txtSolapin.getText().trim();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Número de solapín no válido.", "Solapín Inválido", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtCid.getText().trim().matches("^\\d{11}$")) {
            cid = txtCid.getText().trim();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Número de carné no válido.", "Carné de Identidad Inválido", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!txtNombre.getText().matches("^[A-Za-z\\sñáéíóúÁÉÍÓÚ]+$")) {
            JOptionPane.showMessageDialog(rootPane, "Nombre no válido.", "Nombre Inválido", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!txtNombre.getText().isEmpty()) {
            nombre = txtNombre.getText().trim();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Escriba su nombre completo, por favor.", "Nombre Inválido", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtCorreo.getText().trim().matches("^[a-z0-9!#$%&'*+/=?^_`{|}\\--]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}\\--]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?$")) {
            correo = txtCorreo.getText().trim();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Su dirección de correo no es válida.", "Correo Inválido", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String tipo = (String) comboBoxTipoUsuario.getSelectedItem();
        switch (tipo) {
            case "Estudiante" -> {
                String grupo = txtGrupoOAptoCargo.getText().trim();
                if (grupo.matches("^(IDFT|IDFL|IDFI|IDFO|IDFC|CDFB)\\d{3}$")) {
                    if (Sistema.addUsuario(Sistema.getOperador(), new Estudiante(solapin, cid, nombre, correo, grupo))) {
                        JOptionPane.showMessageDialog(rootPane, "Usuario de nombre " + nombre + " ha sido registrado correctamente en el sistema.", "Operación Exitosa", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "El grupo dado no es correcto. Se espera el identificador de la facultad seguido del número del grupo sin espacios. Por ejemplo:\nIDFT102 - IDFL102 - IDFI102 - IDFO102 - IDFC102 - CDFB102 .", "Grupo Inválido", JOptionPane.ERROR_MESSAGE);
                }
            }
            case "Trabajador/Profesor" -> {
                String cargo = txtGrupoOAptoCargo.getText().trim();
                String area = txtArea.getText().trim();
                if (Sistema.addUsuario(Sistema.getOperador(), new Trabajador(solapin, cid, nombre, correo, area, cargo))) {
                    JOptionPane.showMessageDialog(rootPane, "Usuario de nombre " + nombre + " ha sido registrado correctamente en el sistema.", "Operación Exitosa", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            case "Familiar" -> {
                String apto = txtGrupoOAptoCargo.getText().trim();
                if (apto.matches("^[0-9]+$")) {
                    if (Sistema.addUsuario(Sistema.getOperador(), new Familiar(solapin, cid, nombre, correo, apto))) {
                        JOptionPane.showMessageDialog(rootPane, "Usuario de nombre " + nombre + " ha sido registrado correctamente en el sistema.", "Operación Exitosa", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "El número de apartamento solo puede contener dígitos, no letras.", "Número de apartamento inválido", JOptionPane.ERROR_MESSAGE);
                }

            }
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

