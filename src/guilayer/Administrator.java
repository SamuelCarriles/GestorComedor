    private void btnMarcarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMarcarActionPerformed
        // TODO add your handling code here:
        String solapin = txtSolapinB1.getText().trim();
        if (!Sistema.marcarUsuario(Sistema.getOperador(), solapin)) {
            JOptionPane.showMessageDialog(null, "El usuario ya está marcado.", "Operación Fallida", JOptionPane.WARNING_MESSAGE);
        }
        Usuario u = null;
        for (Usuario us : Sistema.getUsuarios()) {
            if (us.getSolapin().equals(solapin)) {
                u = us;
                break;
            }
        }
        if (u != null) {
            String info = "Nombre de Usuario: " + u.getNombre() + "\nCorreo Electrónico: " + u.getCorreo();
            if (u instanceof Estudiante estudiante) {
                info += "\nTipo de Usuario: Estudiante\nGrupo: " + estudiante.getGrupo();
            } else if (u instanceof Trabajador trabajador) {
                info += "\nTipo de Usuario: Trabajador/Profesor\nCargo: " + trabajador.getCargo() + "\nÁrea de Trabajo: " + trabajador.getArea();
            } else if (u instanceof Familiar familiar) {
                info += "\nTipo de Usuario: Familiar\nApartamento: " + familiar.getNumApartamento();
            }
            txtPane1.setText(info);
        } else {
            JOptionPane.showMessageDialog(rootPane, "El número de solapín no coincide con ninguno de los números de los solapines de los usuarios registrados en el sistema.", "Operación Fallida", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnMarcarActionPerformed

