    private void btnBuscarMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarMActionPerformed
        Usuario u = null;
        for (Usuario us : Sistema.getUsuarios()) {
            if (us.getSolapin().equals(txtSolapinMod.getText().trim())) {
                u = us;
                break;
            }
        }
        if (u != null) {
            txtCidMod.setText(u.getCarnetID());
            txtNombreMod.setText(u.getNombre());
            txtCorreoMod.setText(u.getCorreo());
            switch (u) {
                case Estudiante estudiante -> {
                    comboBoxTipoUsuarioMod.setSelectedIndex(0);
                    labelGrupoAptoCargoMod.setText("Grupo");
                    txtGrupoOAptoCargoMod.setText(estudiante.getGrupo());
                    labelAreaMod.setVisible(false);
                    txtAreaMod.setVisible(false);
                }
                case Trabajador trabajador -> {
                    comboBoxTipoUsuarioMod.setSelectedIndex(1);
                    labelGrupoAptoCargoMod.setText("Cargo");
                    txtGrupoOAptoCargoMod.setText(trabajador.getCargo());
                    labelAreaMod.setVisible(true);
                    txtAreaMod.setVisible(true);
                    txtAreaMod.setText(trabajador.getArea());
                }
                case Familiar familiar -> {
                    comboBoxTipoUsuarioMod.setSelectedIndex(2);
                    labelGrupoAptoCargoMod.setText("Apartamento");
                    txtGrupoOAptoCargoMod.setText(familiar.getNumApartamento());
                }
                default -> {
                    JOptionPane.showMessageDialog(rootPane, "Error desconocido.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            switch (u.getRol()) {
                case "cliente" ->
                    comboBoxRol.setSelectedIndex(0);
                case "empleado" ->
                    comboBoxRol.setSelectedIndex(1);
                case "administrador" ->
                    comboBoxRol.setSelectedIndex(2);
            }
        } else
            JOptionPane.showMessageDialog(rootPane, "El número de solapín no coincide con ninguno de los números de los solapines de los usuarios registrados en el sistema.", "Operación Fallida", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnBuscarMActionPerformed

