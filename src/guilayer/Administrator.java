    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:
        String solapin = txtSolapinC.getText().trim();
        String passAct = new String(txtPassActual.getPassword()).trim();
        String passNew = new String(txtPassNew.getPassword()).trim();
        String passConf = new String(txtPassConf.getPassword()).trim();
        Usuario op = Sistema.getOperador();
        if (op.getSolapin().equals(solapin)) {
            if (op.getPassword().equals(passAct)) {
                if (passNew.equals(passConf)) {
                    if (!Sistema.cambiarPass(Sistema.getOperador(), solapin, passConf)) {
                        JOptionPane.showMessageDialog(null, "Operación fallida. El usuario no existe.", "Operación Fallida", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    txtPassConf.setText("");
                    txtPassNew.setText("");
                    JOptionPane.showMessageDialog(rootPane, "La contraseña nueva y la confirmación de contraseña no coinciden.", "Operación Fallida", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "La contraseña actual no es correcta.", "Operación Fallida", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "El número del solapín no es válido.", "Operación Fallida", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

