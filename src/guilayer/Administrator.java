    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Usuario u = new Usuario();
        for (Usuario us : Sistema.getUsuarios()) {
            if (us.getSolapin().equals(txtSolapinDel.getText())) {
                u = us;
                break;
            }
        }
        if (Sistema.deleteUsuario(Sistema.getOperador(), txtSolapinDel.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Usuario eliminado con éxito.", "operación Exitosa", JOptionPane.INFORMATION_MESSAGE);
            if (txtAreaInfo.getText().equals("Ningún usuario eliminado en esta sesión...")) {
                txtAreaInfo.setText("");
            }
            String info = txtAreaInfo.getText();
            info += "\nNombre de Usuario: " + u.getNombre()
                    + "\nCarné de Identidad: " + u.getCarnetID()
                    + "\nNúmero de Solapín: " + u.getSolapin()
                    + "\nCorreo Electrónico: " + u.getCorreo();
            if (u instanceof Estudiante estudiante) {
                info += "\nTipo de Usuario: Estudiante\nGrupo: " + estudiante.getGrupo();
            } else if (u instanceof Trabajador trabajador) {
                info += "\nTipo de Usuario: Trabajador/Profesor\nCargo: " + trabajador.getCargo() + "\nÁrea de Trabajo: " + trabajador.getArea();
            } else if (u instanceof Familiar familiar) {
                info += "\nTipo de Usuario: Familiar\nApartamento: " + familiar.getNumApartamento();
            }
            txtAreaInfo.setText(info);
        } else {
            JOptionPane.showMessageDialog(rootPane, "El número de solapín no coincide con ninguno de los números de los solapines de los usuarios registrados en el sistema.", "Operación Fallida", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

